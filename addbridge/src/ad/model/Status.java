package ad.model;

public class Status {
	private int s_no;			//상태여부 번호
	private String s_name;		//상태명
	
	public Status() {}
	
	public int getS_no() {
		return s_no;
	}
	public void setS_no(int s_no) {
		this.s_no = s_no;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	
}
